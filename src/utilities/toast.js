import { toast } from 'react-toastify';

export const warningMsg = name =>
  toast.warn(`${name} contact has been removed!`, {
    position: 'top-right',
    autoClose: 3000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: 'colored',
  });

